<h5>Técnico em contabilidades</h5>

Neste curso, o aluno irá aprender a fazer lançamentos contábeis de uma empresa, a executar do controle financeiro de diversos contratos e projetos, entre outros. 
<hr><br>
<h5>Técnica em Edificações</h5> 

Neste curso, o aluno irá aprender a elaborar projetos arquitetônicos, estruturais e de instalações prediais, aplicar normas, métodos, técnicas e procedimentos estabelecidos, visando à qualidade e produtividade dos processos construtivos e segurança dos trabalhadores. 
<hr><br>
<h5>Técnico em Turismo Receptivo</h5>

Neste curso, o aluno irá aprender a receber e acolher turistas em agências de turismo, meios de hospedagem, bares e restaurantes, empresas organizadoras de eventos, hospitais, parques, dentre outros atrativos turísticos e empresas prestadoras de hospitalidade.
