<h5>Técnico em Administração</h5>

Neste curso, o aluno irá aprender a adotar uma postura ética na execução da rotina administrativa, elaborando um planejamento da produção e materiais, recursos humanos, financeiros e mercadológicos, além de ser estimulado a ter ideias e práticas empreendedoras. 
